<p>This app is primarily designed to give students, and of course others too, an easy to use platform to find detailed information about many different materials
    presented in a clear and compact manner.</p>
<p>It is an international student project between german technical engineering students from the University of applied science Wilhelmshaven
    and french material engineering studends from the University of Bordeaux (IUT). </p>
<p>Developement was assumed by Valentin Botcazou, Gaspard Meric, Arne Hude and Dirk Göpf, gathering of materials was undertaken by
